let month = Number(prompt("Введите номер месяца (1-12):"));

const months = [
    "Январь", "Февраль", "Март", "Апрель",
    "Май", "Июнь", "Июль", "Август",
    "Сентябрь", "Октябрь", "Ноябрь", "Декабрь"
];

if (month >= 1 && month <= 12) {
    alert(months[month - 1]);
} else {
    alert("Некорректный номер месяца");
}